public class Gfor {
    public static void main(String[] args) {
        for (int cont = 0; cont <= 3; cont ++) {
            System.out.println("Número " + cont);
        }

        for (int cont = 0; cont <= 10; cont += 2) { 
            System.out.println("Número " + cont);
        }

        for (int cont = 15; cont >= 1; cont -= 1){
            System.out.println("Número " + cont);
        }

        // Estrutura de repetição - for
    }
}